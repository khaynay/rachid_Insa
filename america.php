							<h2>Visa</h2>
							<p>- Most international students will require an F-1 visa. This visa is for international students who have been accepted into a full-time academic degree program at an accredited US College or University</p>
						
							<p>- The US visa requirements process below:</p>
							<ul class="list-group">
							<li class="list-group-item">•	Pay the Machine Readable Visa (MRV) application fee of US$160 (the embassy will tell you where you should pay this).</li>
							<li class="list-group-item">•	Complete a DS-160 form online, uploading a photograph.</li>
							<li class="list-group-item">•	Bring to the interview your acceptance letter; proof you’ve paid the required fees and completed the relevant forms; the Form I-20 or Form DS-2019 to demonstrate your eligibility; and a passport which doesn’t expire until at least six months after you complete your degree, unless you’re exempt due to country-specific agreements.</li>
							<li class="list-group-item">•	The decision to issue a visa is in the hands of the consular officer. Their decision will hinge on three things: whether you can financially support yourself, whether you can show you can and want to return home after graduating (getting a work visa is a whole different process), and whether you can prove your academic results to date.</li>
							<li class="list-group-item">•	The last of these may involve proving your proficiency in English. You will also have to provide evidence of this to your university at the application stage.</li>
							<li class="list-group-item">•	Additional documents may be requested to establish your academic preparation, your intent to depart the US upon completion of your course of study, and how you will cover your costs. Make sure you have these with you when you go to the visa application interview.</li>
							<li class="list-group-item">•	Additional demands will be made if you have a criminal record, and being granted a visa doesn’t guarantee entry into the US.</li>
							<li class="list-group-item">•	Your local US embassy should be able to tell you the expected wait time for your visa. You can receive your US study visa a maximum of 120 days before your course’s start date, but processing can commence before this date.</li>
							<li class="list-group-item">•	You cannot actually enter the country until 30 days before the beginning of your course, unless you have a visitor visa. This last stipulation also applies to those who would normally not require a visa for short stays in the US.</li>
							</ul>
							
							
