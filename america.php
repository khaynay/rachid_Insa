						<div class="text-box">
							<h2>How to Apply to an American University</h2>
							<p>- To start your application to study in the US, you will need to apply directly to each of the universities you’re interested in.</p>
							<p>- Entry requirements for each university are different, but most involve completing an admissions test or essay, the SAT or ACT admissions tests, and providing recommendation letters from teachers. On top of this, most universities ask for a transcript of your grades and a personal statement.</p>
							<p>- Undergraduate application deadlines for a start in semester one of the American academic year (September) are from January-March of the same year, with most generally falling in January.</p>
							<p>- Students needing to provide admissions and English language proficiency test scores as part of their applications will need to have them handy in time for this date.</p>
							<p>- There is also a centralized application process called the <a href="https://www.commonapp.org/" target="new">Common Application</a> that is used by over 700 American universities. Almost two thirds of universities using the service however require that applicants submit additional documents directly to them. </p>
							
							
							
