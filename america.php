							<h2>System of Education</h2>
							<p>- Also called ‘junior colleges,’ community colleges offer two-year programs called Associate Degrees that may be used as a bridging qualification to a four-year Bachelor degree.</p>
							<p>- Costs of studying at a community college also average to be less than half of those at a public university, and about one-tenth of those at a private university.</p>
							<p>- Public institutions are state universities that are funded by that state’s government, and offer studies in most general areas. Tuition fees are lower for students from that state. Private universities are often pricier, more selective and offer more specialist subjects or facilities. </p>
							<p>- Bachelors degrees in America follow a general education model, meaning that students will complete a study in a general field within which they will specialize in a particular area that is called their ‘major.’</p>
							<p>- Completing a degree program can take as long to complete as it takes a student to meet all the credit requirements.</p>
							<p>- International students must take at least 12 credits per semester to maintain their visa status. Most undergraduate programs are designed for completion across four years for full-time students.</p>
							<p>- Masters degrees generally require completion of six-eight advanced courses, as well as an intensive study project and/or a thesis.</p>
						</div>
						
						
					</div>
