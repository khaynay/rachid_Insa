						<h2><span>Why Australia?</span></h2>
						<h5><b>Pros</b></h5>
						<p>-A global leader in technology and economics.</p>
						<p>-Australian education is not only of an incredibly high standard but offers study options across all study levels</p>
						<p>-Australian universities are incredibly flexible in their degree options, allowing students to manage and spread out their study credits around part-time work or other commitments</p>
						<p>-5 out of the nation’s cities were voted within the best 30 student cities in the world.</p>
						<p>-Qualifications from Australian universities are highly regarded and globally recognized by employers and institutions alike.</p>
						
						<br>
												<h5><b>Cons</b></h5>
						<p>-Cultural shock/differences can be a problem for some.</p>
						<p>-Though financial help is provided, the cost of education is slowly rising each and every year which is becoming difficult for students to afford education.</p>
						
						
					</div>
					
					<div class="image-caption-box">
						<div class="text-box">
							<h2>System of Education</h2>
							<p>-Australia’s higher education sector is comprised of both technical colleges and universities, offering technical, undergraduate and postgraduate qualifications.</p>
							<p>-The Australian academic year for universities is split into 2 'sessions'. The year begins end of February/beginning of March with the Autumn session which goes on until mid-term break in April.</p>
							<p>-Session 2 begins in July/August with a mid-term break in September/October.</p>
							<p>-General bachelor degrees in Australia are 3 years in length, with an option to do a fourth year called ‘Honours.</p>
							<p>-Acceptance into honours is based on academic merit, and is comprised of taught coursework and a research-based thesis component.</p>
							<p>-Many universities also offer double degree programs, in which students are able to undertake units from two different degrees at a time. As a result, double degrees are much longer than a standard undergraduate study.</p>
							<p>-Australian universities expect students to take initiative and are focused on independent learning. </p>
							
						</div>
						
						
					</div>
					<div class="image-caption-box">
						<div class="text-box">
							<h2>How to Apply to an Australian University</h2>
							<p>-Intake periods are different across undergraduate and postgraduate levels of study, and vary in specific dates for different universities.</p>
							<p>-Australian universities will have two intake periods: one at the start (February) and one in the middle (July) of the academic year.</p>
							<p>-You can apply directly to a university through their website, as well as through the Universities Admissions Centre (UAC).</p>
							<p>-Australian universities will require you to include your resume, personal statement, reference letters, all previous academic transcripts and IELTS score card and other documents as specified by your specific home or host institution.</p>
							<p>-Students may also need to sit additional admissions and aptitude tests, depending on the requirements of their institution and program.</p>
							<p>-If your application is successful, you’ll receive a ‘Letter of Offer’ which you should read carefully before signing and sending it back to the university to confirm your acceptance.</p>
							<p>-After you’ve accepted your offer and paid your deposit you will receive an ‘Electronic Confirmation of Enrolment’ (eCoE) by email, which outlines your program’s start date, total fees and course length.</p>
							
						</div>
						
					</div>
					
					<div class="image-caption-box">
						<div class="text-box">
							<h2>Visa</h2>
							<p>-When you study abroad in Australia, the student visa you’ll need depends on what type of study you’ll be undertaking.</p>
						
							<p>-Each visa comes under a different official ‘Subclass,’ which just refers to how it is categorized by the government.</p>
							<p>-Prospective students are advised to verify how their program is classified and what visa they will need with their specific institution.</p>
							<p>-Students must first be accepted for full-time study before they begin the application process, and be able to prove their status via electronic confirmation or enrolment letter from their host institution.</p>
							<p>-After you’ve been offered a place at an Australian university and paid your deposit, you will receive an ‘Electronic Confirmation of Enrolment’ (eCoE) via e-mail, detailing your total course fee, duration and start date.</p>
							<p>-You must be able to demonstrate that you can cover the set AU$18,610 minimum annual living costs, as well as tuition fees and air fares</p>
							<p>-You can check exactly how much you’ll have to pay by looking at the visa pricing table.</p>
								
						</div>
						
					</div>
					
					<div class="image-caption-box">
						<div class="text-box">
							<h2>Cost of living and scholarhships</h2>
							<p>-If you’d like to live on campus in university accommodation, this will typically cost A$90 to A$280 per week (~US$70-210).</p>
							<p>-You could also choose a homestay, in which you’ll live with an Australian family at their home, with bills and meals usually included in your rent, usually $235 to $325 per week (~US$180-250).</p>
							<p>-The HSBC report says the average cost per year of study in Australia for international students is AU$42,093 (~US$32,950). This breaks down to AU$24,081 (US$18,850) for fees and AU$18,012 (US$14,100) for living costs.</p>
							<p>-However, tuition fees at Australian universities will vary considerably depending on where and what you study, and the level you will be studying at.</p>
							<p>-You can search for a scholarship from the Australian government on the Study in  <a href="https://www.studyinaustralia.gov.au/" target="new">Australia website</a>, or commandeer their handy scholarship search engine tool to search for alternate options that are granted externally.</p>
							<p>-You can search for externally granted awards using scholarship search engines such as JASON (Postgraduate funding only) and <a href="http://www.hobsonscoursefinder.com.au/scholarship/search#.U0uXO7TwrbM" target="new">Hobsons Course Finder</a>, or simply search a study area or company name and pursue your options from there.</p>
							
