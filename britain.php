							<h2>Cost of living and scholarhships</h2>
							<p>-Costs vary hugely across institutions but the average cost for full time education in England was calculated to be around £8,600 per year.</p>
							<p>-On average international students can expect to pay within the following ranges per course, by subject area:</p>
							<ul class="list-group">
							<li class="list-group-item">•	Humanities and Social Sciences...£9,000-£10,800</li>
							<li class="list-group-item">•	Sciences and Engineering...£10,200-£13,800</li>
							<li class="list-group-item">•	Clinical Subjects...£23,000-£26,000</li>
							</ul>
							<p>-You’ll need at least £12,000 a year to cover living costs in the UK (~US$14,720) and will need to budget more to live in London, where rent and other costs are considerably higher than in the rest of the UK.</p>
							<p>-There are a limited number of scholarships awarded to international students by the UK government. The main award granted is the <a href="http://www.chevening.org/apply" target="new">Chevening Scholarship Programme</a></p>
							<p>-Almost all universities in the UK will offer some kind of funding to international students. However, funding is more commonly offered to Postgraduate students, as their specific areas of focus (particularly in research programmes) are of a high value to the university.</p>
							<p>-Scholarships are typically awarded based on academic merits.</p>
							<p>-As well as scholarships, many UK institutions offer funding in the way of bursaries, reduced fees or one-time payments for students of particular circumstances.</p>
							<p>-External funding possibilities are virtually endless, and can be tracked down from anything as simple to an internet search of your study area to using a scholarship search engine such as The <b>Scholarship Hub, the Postgraduate Funding Guide and Scholars4Dev.com.</b> </p>
							
							
