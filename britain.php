							<h2>System of Education</h2>
							<p>-Higher education institutions in the UK are either degree-awarding (‘recognized bodies,’ called universities and colleges) or non-degree awarding, but provide bridging courses for entry into a degree-awarding program (‘listed bodies’).</p>
							<p>-Recognized bodies are permitted to award students Bachelor, Masters and Doctorate qualifications, as well as selected other higher education awards depending on the nature of the institution.</p>
							<p>-A number of qualifications are offered in the UK at undergraduate level: Bachelors degree, Foundation degree, Diploma of Higher Education (DipHE), Certificate of Higher Education and Higher National Diploma (HND).</p>
							<p>-A Bachelors degree usually takes three years to complete for a full-time student.</p>
							<p>-A Foundation degree combines academic coursework with practical, work-based learning with an employer.</p>
							<p>-Masters degrees are either taught or researched-based programs that are typically one year in duration, whilst Doctorate programs may take up to seven years to complete.</p>
							
						</div>
						
						
					</div>
					<div class="image-caption-box">
						<div class="text-box">
							<h2>How to Apply to a UK University</h2>
							<p>-The application process to study abroad in the UK differs slightly for EU and non-EU students, but in most instances is done through the Universities and Colleges Admissions Service (UCAS), the official UK application service.</p>
							<p>-If your institution is not listed with UCAS, you will need to contact them directly to confirm specific application procedures.</p>
							<p>-The website provides all the details on how to apply, what to include, how to track your application and how to respond to your chosen universities.</p>
							<p>-Once you’ve submitted your application, UCAS will send it to the institutions you’ve chosen, and then email or mail you back their response.</p>
							<p>-All students are required to write a ‘personal statement’ explaining their reasons for wanting to study their chosen subject.</p>
							<p>-International students may need to send copies of their academic transcripts to their course provider.</p>
							<p>-Though UCAS processes the applications, decisions about admissions requirements are made by individual universities.</p>	
						</div>
						
					</div>
					
					<div class="image-caption-box">
						<div class="text-box">
							<h2>Visa</h2>
							<p>-When you are accepted into a UK institution, they will send you a reference number called your Confirmation of Acceptance of Studies (CAS). You will need this document to apply for your student visa.</p>
							<p>-After you have received your CAS, there are three steps in applying for a UK student visa: completing the online application form (and paying the application fee), visiting a visa application center to have your fingerprints and photograph taken, and posting your complete application to your nearest British embassy or consulate.</p>
							<p>-All students whose study program is longer than six months will need a Tier 4 (General) student visa.</p>
							<p>-Students completing some Science and Technology Undergraduate, Masters or PhD programs may also need to apply for an Academic Technology Approval Scheme (ATAS) certificate that you will need BEFORE you complete your student visa application.</p>
							<p>-You’ll need to meet the financial requirements set by the UK Border Agency (UKBA).</p>
						
							
						</div>
						
					</div>
					
