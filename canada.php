						<div class="text-box">
							<h2>How to apply to a Canadian university?</h2>
							<p>-Apply directly to the university you’re interested in.</p>
							<p>-Most Canadian higher education institutions require the completion of a high school diploma.</p>
							<p>-International students may also be asked to provide proof of language proficiency, either in English or French.</p>
							<p>-There is two main intakes in which international student can apply: one in September and one in January.</p>
							<p>-The application process should be undertaken at least 6 months before the deadline set by the university.</p>
							<p>-The documents required vary depending on the universities</p>
							<p>-Once you have received an Offer of Admission letter, you will have a deadline to accept this offer</p>
							<p>-Once you have been made an offer by a university, you can apply for your study permit.</p>
							<p>-Most applications will require a non-refundable fee of $50-100 (CAD)</p>
							<p>-Edu Canada (http://www.educanada.ca/study-etudes/step-etape.aspx?lang=eng) enables students to have an idea on the cost of studies depending on the university of your choice</p>
						</div>
						
					</div>
					
					<div class="image-caption-box">
						<div class="text-box">
							<h2>Visa</h2>
							<p>-You must apply for a student permit online at the official Citizen and Immigration Canada. (CIC) website</p>
							<p>-Once you have been offered a place by an accredited Canadian education institution, you can apply for your student permit.</p>
							<p>-You cannot obtain a student permit if you have a criminal record so a certificate to show that you haven’t got one might be required.</p>
							<p>-You will need to provide documentation to show that you can fund your studies as well as having the funds to support yourself while living in Canada through bank statements, proof of payment of accommodation or tuition fees, and scholarship or bursary notifications.</p>
							<p>-Currently you must show that you have access to $10,000 (CAD) for each year of your stay, to show you can support yourself. (tuition fees are not included).</p>
							<p>-The price of your permit will be confirmed to you before you pay and submit your application. Approximately, a Canadian student permit is $150 (CAD).</p>
							<p>-In some cases you may be required to attend an interview in person at your local Canadian visa office in your country.</p>
							
						</div>
						
					</div>
					
