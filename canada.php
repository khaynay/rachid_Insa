							<h2>Cost of living</h2>
							<p>-3000-$7500 per year for on-campus accommodation.</p>
							<p>-On-campus accommodation fees include electricity, water and heating.</p>
							<p>-For off campus accommodation you can use resource like Easy Roommate or Roomster to find a roommate or an accommodation.</p>
							<p>-Rent prices will vary from city to city with larger areas like Calgary, Toronto and Vancouver being most expensive</p>
							<p>-It’s suggested you budget around CAD$250-1500 per month for housing that is off-campus.</p>
