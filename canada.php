						<div class="text-box">
							<h2>System of Education and Post-Study</h2>
							<p>-Undergraduate degrees in Canada can take either three or four years to complete, depending on the university. </p>
							<p>-Postgraduate degrees last between one and three years to complete, depending on the type of degree.  </p>
							<p>-As universities in Canada are managed by provincial governments, you’ll find there will be slight differences in how education is carried out. Quebec in particular is markedly different to the rest of Canada, with different term times and length of study. For instance, students finish secondary school a year early and must take a mandatory pre-university ‘General and Vocational College’ (CEGEP) course, essentially eliminating the freshman year of university. </p>
							<p>-Plus because Canadian universities receive public funding, tuition fees are less than you would expect at an American university. </p>
							<p>-In Canada, international students who graduate from a university or college can work in Canada for one year after receiving their qualification. There is also some freedom for international students to work while studying. </p>
							<p>-90% of graduates from Canadian institutions find a job within 6 months. </p>
						</div>
						
						
					</div>
