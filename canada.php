						<h2><span>Why Canada?</span></h2>
						<h5><b>Pros</b></h5>
						<p>-A total of 26 universities in Canada feature in the QS World University Rankings® 2016-2017,</p>
						<p>-Healthcare is free in Canada unlike in America, which is a popular criticism of the country, especially amongst conscientious international students.</p>
						<p>-For a major western country boasting several large cities, the cost of living in Canada is much cheaper than in the UK or America. </p>
						<p>-Canada is also considered one of the safest and most peaceful study destinations, especially compared to their American cousins; this is something which reassures many parents about sending their kids there to study.</p>
						<p>-Canada is a nation of immigrants, welcoming over 15 million in the last century and a half.</p>
						<br>
												<h5><b>Cons</b></h5>
						<p>-Much of the country can be very cold in the winter. Except the Vancouver area, the rest of Canada put up with 4–5 months of very cold weather, shoveling snowy driveways, dealing with nasty winter roads and high heating bills in the winter.</p>
						<p>-Taxes are fairly high, the average Canadian pays about 42% in personal income tax and by the time GST, fuel taxes, capital gains, property tax and sin taxes on alcohol and tobacco, we are up near 50%.</p>
						<p>-Tuition for post-secondary schooling is fairly high</p>
					</div>
					
