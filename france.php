						<h5><b>Pros</b></h5>
						<p>-The French are passionate about higher education and invest heavily in research, meaning its universities consistently perform well in global university rankings</p>
						<p>-Perfect opportunity to sharpen up your French language skills.</p>
						<p>-The cost of higher education in France is quite reasonable compared to other popular study destinations such as the UK and the USA. </p>
						<p>-Funding and grants are also available and the French Ministry of Foreign and European Affairs allocates a large number of grants to international students.</p>
						<p>-it's important to note that the cost of living can be considerably higher, particularly in cities like Paris where food and accommodation can be pricey. </p>
						<p>-The higher education system is underpinned by strong government support and research departments and business schools are some of the best in the world.</p>
						<p>-French and international students pay the same tuition fees</p>
