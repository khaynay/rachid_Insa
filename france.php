							<h2>Cost of living and scholarhships</h2>
							<p>-The average cost of housing in France is €250-700 per month, although this rate is greatly affected by the region that you stay in, with major cities like Paris being the most expensive.</p>
							<p>-If your university or grandes écoles offers the option of a university residence, in most instances you’ll able to set this all up before you arrive by contacting your institution or international officer. The average cost of a university is between €250 and €350 per month.</p>
							<p>-On average the of living of a student in France is 8000 euros.</p>
							<p>-Several Scholarships are available for international students that will greatly help them in covering their tuition fees. Among them we find:</p>
							<p><b>Emile Boutmy Scholarships:</b> Sciences Po created the Emile Boutmy scholarship after the founder of Sciences Po (1871) in order to welcome the very best international students from outside of the European Union. The Emile Boutmy Scholarship is awarded to top students whose profiles match the admissions priorities of Sciences Po and individual course requirements.  The tuition grant is up to 7300 euros per year.</p>
							<p><b>The Eiffel Excellence Scholarship:</b> The Eiffel Excellence Scholarship Program was established by the French Ministry of Foreign Affairs and International Development to enable French higher education establishments to attract top foreign students to enroll in their master’s and PhD courses.</p>
							<p><b>Erasmus Mundus Scholarships for International Students:</b> Funded by the European Union, the Erasmus Mundus Scholarships for International Students are available to both EU and non-EU students that have been selected to attend one of the Erasmus Mundus Joint Programs at Masters or Doctorate level.</p>
