												<h5><b>Cons</b></h5>
						<p>- Educational culture-shock: Things are not going to be like they were in your country with respect to lectures/seminars. At the start of a course, you can be as many as 500–700 in lecture halls. Finding spaces to sit can become a bit of a lottery. The exams take on a special form of importance, as they are the primary means of cutting down the class sizes.</p>
