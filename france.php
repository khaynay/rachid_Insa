							<p>-A Bachelor degree, known as ‘Licence’ lasts 3-4 years</p>
							<p>-Master’s degree usually last 2 years</p>
							<p><b>Public Universities</b> (The French government funds public universities and it also oversees all regulation of the national diploma granted by these universities. Public universities offer an affordable but high quality education and are popular among international students.)</p>
							<p><b>Private Universities</b> (tuition fees at private institutions are predictably much more expensive than at government-funded public universities. However, these specialized institutions often offer the best teaching and training within their fields.) </p>
							<p><b>Grande école</b> (Grandes Écoles are high quality higher education institutions that run alongside the public and private university system in France. They have a competitive outlook and admission into these schools is often more challenging.)</p>
