							<h2>Visa</h2>
							<p>-Once accepted by a French institution, you’ll need to apply for a visa, which also includes a residence permit, called the VLS-TS, this option will cover your entire stay of your studies (three-four years).</p>
							<p>-If the universities you are applying to in France require you to sit an entrance exam in order to gain admission, you can apply for a “visa de court séjour étudiant concours”. If you are successful in your entrance exam, you’ll be able to apply for a one-year residence permit.</p>
							<p>-The Documents you will need are the following:</p>
							<ul class="list-group">
							<li class="list-group-item">An acceptance letter onto an accredited program of study at a French institution </li>
							<li class="list-group-item">Proof of funds. This includes covering living costs and can be proven with a bank statement, letter from a guarantor or document stating your funding, grant or scholarship details. </li>
							<li class="list-group-item">Proof of return ticket home. If you don’t have your ticket home yet you can also submit a handwritten statement of intent detailing planned departure dates. </li>
							<li class="list-group-item">Proof of medical insurance (minimum insurance €30,000) </li>
							<li class="list-group-item">Proof of accommodation. This could be any official confirmation from your landlord in France or a certificate detailing your student residence arrangement. </li>
							<li class="list-group-item">Proof of language proficiency. If you’re applying for a course with components in French, you’ll need to prove you have obtained a certain level of the language. You can contact the international office of your university for more information on this.</li>
							</ul>
							<p>-You will need to provide documentation to show that you can fund your studies as well as having the funds to support yourself while living in Canada through bank statements, proof of payment of accommodation or tuition fees, and scholarship or bursary notifications.</p>
							<p>-Currently you must show that you have access to $10,000 (CAD) for each year of your stay, to show you can support yourself. (tuition fees are not included).</p>
							<p>-The price of your permit will be confirmed to you before you pay and submit your application. Approximately, a Canadian student permit is $150 (CAD).</p>
							<p>-In some cases you may be required to attend an interview in person at your local Canadian visa office in your country.</p>
							
