					<div class="sec-title">
						<h2><span>Why Germany?</span></h2>
						<h5><b>Pros</b></h5>
						<p>-Germany is the world’s most popular non-Anglophone study destination, and the third most popular overall – only the US and the UK welcome more international students each year.</p>
						<p>-Universities in Germany offer plenty of choice, including some of the most prestigious institutions in Western Europe.</p>
						<p>-Every major German city you can think of has at least one university ranked among the world’s best.</p>
						<p>-Studying in Germany at undergraduate level is FREE in public universities (however In December 2016 it was announced the south-west state of Baden-Württemberg will be reintroducing tuition fees for non-EU students, starting from autumn 2017, and it’s possible other states will follow suit in the coming years.)</p>
						<p>-If you complete your undergraduate studies at a German institution, you may even be eligible for a free postgraduate program too.</p>
						<p>-Germany also offers high quality of life, extensive support and scholarship schemes for international students</p>
						<p>-After graduating, international students are allowed to stay up to 18 months in Germany to look for a job</p>
						<p>-Over 12% of the student population at institutions are international.</p>
						
						<br>
												<h5><b>Cons</b></h5>
						<p>- Language: the chances of finding a decent job is also difficult without the language, especially for some sectors. You have to learn German language.</p>
					</div>
					
					<div class="image-caption-box">
						<div class="text-box">
							<h2>System of Education </h2>
							<p>- There are three different types of universities: Universities (Universitäten), Universities of Applied Sciences (Fachhochschulen), Universities of Art, Music and Film (Kunst- und Musikhochschulen).</p>
							<p>-While tuition costs are free, however, students are required to pay a nominal fee per semester. This fee covers administration, student support and other costs, and is usually no more than €250 (~US$280).</p>
							<p>-For most subjects, you can apply directly to the international office of the university. Alternatively, you can use uni-assist, a centralized admissions portal for international students. This is one of the services run by the German Academic Exchange Service (DAAD).</p>
							<p>-If you want to study in Germany at master’s level, you may need to pay tuition fees. This is usually only the case for students who have not completed a related bachelor’s program at a university in Germany.</p>
							<p>-Fees for non-consecutive master’s degrees vary, but can amount to as much as €10,000 (~US$11,100) per semester.</p>
							
						</div>
						
						
					</div>
					<div class="image-caption-box">
						<div class="text-box">
							<h2>How to Apply to a German University</h2>
							<p>- Many German universities offer the option of applying twice a year, with courses commencing either in winter or summer.</p>
							<p>-In general the documents required are: A certified copy of your higher education entrance qualification, such as a high-school diploma or certificate (in the original language), A translated overview of the subjects and grades of your qualifications (in English), A passport photo, A copy of your passport (personal information and photo ID) and Proof of English language proficiency.</p>
							<p><b>NB:</b> the process of applying to university in Germany varies from institution to institution, it’s essential that you visit the university website, in particular the university’s International Office (Akademisches Auslandsamt) to find out specific information on applying, deadlines and procedures. </p>
							
							
							
				
						</div>
						
					</div>
					
					<div class="image-caption-box">
						<div class="text-box">
							<h2>Visa</h2>
							<p>- The requirements to get a student visa for Germany depend on your country of origin.</p>
							<p>-If you want to study a degree abroad (both undergraduate and postgraduate) you will need to apply for a National Visa or long-term visa. This is a visa for any student looking to stay in Germany for more than 90 days. The first step is to set up an appointment at the Germany embassy in your home country. You’ll then be told exactly what documentation you’ll need.</p>
							<p>-In general the documents required are: valid passport, Proof of funds for living expenses, Confirmation of health insurance coverage, For a student visa: the notice of admission from your university.</p>
							<p>- The general fee for any type of visa to visit Germany is around €60.</p>
							<p>- You may also have to show proof that you don’t have a criminal record and are free of certain diseases.</p>
							<p>- For more details on applying for a German student visa check out the following DAAD detailed <a href="https://www.daad.de/medien/deutschland/stipendien/formulare/info_entry_and_residence.pdf" target="new">visa information pack.</a></p>
							
						
						</div>
						
					</div>
					
					<div class="image-caption-box">
						<div class="text-box">
							<h2>Cost of living</h2>
							<p>-Around 800 euros a month for living expenses.</p>
							<p>-More than 40% of international students stay in student residences which cost around €240 a month, they are relatively cheap and provide a great environment to meet people.</p>
							<p>-Rates vary depending on the type of residence, whether it's a large residence or smaller student flat, the size of your room, and the quality and quantity of furnishings.</p>
							<p>-To apply for a room you should contact the Student Union of your college studentenwerke.de or the DAAD (German Academic Exchange Service), which has an accommodation finder service.</p>
							<p>-More than 30% of students choose shared accommodation, this is usually a shared flat with other students. You each pay for a room, you share the communal facilities and you share the bills, such as the cost of energy and the internet. The average cost is about €280 a month.</p>
							<p>-To find shared accommodation you’ll need to check local newspapers, university notice boards and the internet, as this is where private landlords advertise vacancies. </p>
							<p>-Having your own accommodation is usually the most expensive option, when you rent an entire apartment, it usually does not even include the furniture!</p>
							<p>-Demand and costs are highest in western cities compared with cities in the east of Germany where it's slightly cheaper.</p>
							
							<br>
							
							<div class="image-caption-box">
						<div class="text-box">
							<h2>Scholarships</h2><br>
							<p><b>Government funded</b><br>
					Each year the German government allocates a certain amount of money for scholarships for inbound international students to study in the country.</p>
							<p>DAAD Scholarships<br>
The German Academic Exchange Service (DAAD) offers the widest range of government-funded scholarships at different degree levels. It also has an easy-to-use <a href="https://www.daad.de/deutschland/stipendium/datenbank/en/21148-scholarship-database/?status=&origin=&subjectGrps=&daad=&q=&page=1&back=1" target="new">scholarship database</a> to get you on your way. 
</p>
							<p>Erasmus +<br>
If you’re from Europe, you may find yourself eligible for an Erasmus+ grant. Funded by the European Commission, Erasmus+ offers funding towards living costs while on an exchange program within Europe. Interestingly enough, Germany is one of the most popular study destinations for Erasmus students. Find out more by <a href="http://eacea.ec.europa.eu/erasmus_mundus/funding/scholarships_students_academics_en.php" target="new">clicking here.</a>
</p>
				<p><b>Non-government funded</b><br></p>			
				<p>There are a number of non-government funded scholarships also available for international students. These take the form of various research and academic disciplines and are usually awarded on a merit basis.</p>			
				<p>Heinrich Böll Scholarships for International Students<br>
Both domestic and international students are eligible for grants at undergraduate, Masters or PhD levels with the Heinrich Böll Foundation. There are two application dates a year. <a href="http://www.boell.de/en/foundation/application" target="new">Apply online now!</a></p>			
				<p>Deutschland Stipendium<br/>
High-achieving international students might find themselves eligible for funding of €300 a month through this program. Based on a public-private partnership, businesses, organizations and individuals sponsor talented students for €150 a month which is then matched by the federal government. <a href="http://www.deutschlandstipendium.de/de/2319.php" target="new">Click here</a> to find out more. 
</p>
<p>Kurt Hansen Science Scholarships<br>
International students planning on becoming educators in science are open to apply for this scholarship, which is funded by the Bayer Foundation. The available funding areas include study projects, internships, summer courses, preparatory and supplementary study courses and training of science teachers. <a href="" target="new">Click here</a> to find out more.
</p>			
							
						
