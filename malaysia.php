						<div class="text-box">
							<h2>System of Education and Post-Study</h2>
							<p>- Bachelor degrees in Malaysia are typically between 3-5 years in length for full time students.</p>
							<p>- Some universities offer students the chance to gain an Honours (Hons) qualification as part of their Bachelors. Entry criteria are unique to each program, but will always require a high academic standard and typically add a year onto the student’s Bachelor degree.</p>
							<p>- Honours students will often need to complete a long-form research task in their final year such as a thesis or independent research project. Students need to apply separately for an Honours program, and should research separate application dates and deadlines thoroughly. </p>
							<p>- Most programs at private universities are taught in English, whilst Bachelor programs at public institutions are primarily taught in the Malay or Bahasa Melayu languages.</p>
							<p> - Malaysian Masters programs can take between 1-5 years to complete for full time students, depending on particular credit requirements. PhDs may take up to four years.</p>
							<p>- Dates in the Malaysian academic year vary from institution to institution.</p>
							<p>-A student’s ‘major’ is a specific area they have chosen to focus on within their general field of study. Students must complete a certain quota of subjects in this area in order to be gain the title</p>
							<p>-A ‘minor’ is a secondary area of focus within a general study field with a lower requirement quota.</p>
							<p>-Malaysian universities offer students the unique opportunity to complete their Bachelor’s degree across two partnering institutions in different countries. In the ‘2+1’ twinning program, students may choose to gain course credit from a reputable university in countries such as the UK, Australia, Canada, New Zealand, France or Germany</p>
							<p>-Students also have the option to complete a ‘3+0’ foreign Bachelors program, in which foreign study programs are conducted by institutions in Malaysia.</p>
							<p>-Students pay Malaysian tuition fees throughout their entire undergraduate program, regardless of host country or nature of twinning program.</p>
							
						</div>
						
						
					</div>
					<div class="image-caption-box">
						<div class="text-box">
							<h2>How to Apply to a Malaysian University</h2>
							<p>-Before applying to a university, international students must make sure that the institution has obtained correct accreditation status from the Malaysian Qualifications Agency (MQA).</p>
							<p>-Intake dates differ across institutions, study levels and areas.</p>
							<p>-In most universities, the first semester starts in late February, while the second semester starts in late July.</p>
							<p>-Some universities have separate application dates for international students, it’s therefore important to confirm relevant deadlines with the institution.</p>
							<p>-Most universities application can be completed online directly through the institution’s website.</p>
							<p>-Generally speaking, you will need to submit certified copies of all relevant academic transcripts, a photocopy of your passport (all pages), financial ability evidence, a photocopy of your ID, a passport sized color photograph with a specific background, personal medical health report and relevant application fees. You may also be required to prove your level of proficiency in English (IELTS).</p>
							<p>-Students applying to study at postgraduate level may also be required to submit research or thesis proposals.</p>
							<p>-If you have been admitted, an Offer of Place letter will be sent to you, you will need to letter in order to apply for a student pass at the Malaysian Immigration Department once in Malaysia.</p>
								
						</div>
						
					</div>
					
					<div class="image-caption-box">
						<div class="text-box">
							<h2>Visa</h2>
							<p>-All international students will need a valid Student Pass and Visa to study abroad in Malaysia.</p>
							<p>-Once you’ve been accepted by a Malaysian institution they will submit an application for a Student Pass on your behalf. You will need to send them the required documentation and fees.</p>
							<p>-Depending on where you’re from, you may also be asked to submit additional documents.</p>
							<p>-Students should send their documents ahead of time as they will not be allowed to enter Malaysia without a letter of approval.</p>
							<p>-Once you have an approved provisional Student Pass, you will be issued a student visa upon your point of entry into Malaysia.</p>
							<p>-When booking your flight, it is necessary to give your exact arrival details to your institution as a representative from your university will be waiting to meet you at customs.</p>
							<p>-A Special Pass that will enable you to enter the country which is valid two weeks. You will need to go to your nearest State Immigration Department before that deadline is up to get your full Student Pass.</p>
							<p>-The Student Pass is to be renewed yearly.</p>
							<p>-Yearly fees for a Student Pass are RYM 60 (18 USD$), and visa fees are between RYM 15 – 90 RYM (4.50 – 27 USD$) depending on your country of origin.</p>
							<p>-International students also need to purchase one of three health insurance packages as part of their Student Pass application.</p>
				
							
						</div>
						
					</div>
					
