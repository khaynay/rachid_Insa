						<h2><span>Why Malaysia?</span></h2>
						<h5><b>Pros</b></h5>
						<p>-An international standard and high quality education.</p>
						<p>-Malaysia is seeing more and more professors coming to teach from countries which have traditionally been viewed as education heavyweights in the world, like America and the UK.</p>
						<p>-Wide usage of English, which makes living and studying easy for students who are proficient in English.</p>
						<p>-Hassle-free immigration procedures, which enable foreign students to have easy entry into Malaysian higher educational institutions.</p>
						<p>-Affordable living expenses, which can be as low as RM10,000.00 (USD3,750) per year.</p>
						<p>-Student-friendly laws, which allow international students to work part-time for a maximum of 20 hours per week, while studying full time in Malaysia.</p>
						<p>-Foreign Branch Campus Universities operating in Malaysia, which enable students to acquire their prestigious university qualifications in a country that has lower living expenses.</p>
						<p>-Malaysia has been voted one of the top twenty most peaceful countries in the world,</p>
						<p>-A technological hub.</p>
						
						<br>
												<h5><b>Cons</b></h5>
						<p>-Language.</p>
						
