						<div class="text-box">
							<h2>Cost of living and Tution Fees</h2>
							<p>- Malaysia is cheaper than many countries, with average tuition fees currently standing at approximately US$3,985 per year.</p>
							<p>- Although the prices of programs at Malaysia’s international branch campuses tend to be higher, they are still much cheaper than their international counterparts, while offering the same prestige. For example, if you were to take a bachelor’s degree in computer science from Monash University’s Malaysian branch campus it would cost US$9,222 a year, while at the original Monash University in Australia the price would be more than double, currently US$23,297 a year.</p>
							<p>-The cost of on campus accommodation can be anywhere from RM 350.00 to RM 650.00 a month.</p>
							<p>-Off campus accommodation consist of housing areas that consist of houses (single/double storey), apartments and condominiums which may have 3 or 4 bedrooms together with a living hall, kitchen and bathrooms.</p>
							<p>-The landlord may require the students for a minimum one-year contract (lease) agreement with a deposit of 2 or 3 months’ rental. The rentals are to be paid monthly and other utilities such as water, electricity; gas and cable TV are to be paid by the students themselves according to the monthly usage.</p>
							<p>-The cost of off campus accommodation per student can be anywhere from RM 150.00 to RM 3000.00 a month depending the area of living, type, furniture, facilities and number of students sharing.</p>
							<p>-You can expect a bedroom in an off-campus shared flat to be around RM350-RM500 per month, and an off-campus studio flat to cost about RM1500-RM2500 per month.</p>
							<p>-The cost of living for international students in Malaysia is as low as RM 10,000-12,000 (~US$2,370-2,840) per year, including food, travel and accommodation.</p>
							<p>-Student meals expense is estimated to be around RM500 to RM800 a month the estimated cost is for three decent meals. For those who love to cook and share among the housemates the cooking expenses can be lower as the cost can be shared among your housemates.</p>
							
