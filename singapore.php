						<h2><span>Why Singapore?</span></h2>
						<h5><b>Pros</b></h5>
						<p>-Universities in Singapore are gaining growing recognition.</p>
						<p>-Endless opportunities for students to build strong international networks and grow professionally.</p>
						<p>-Strong economy.</p>
						<p>-In 2014 Singapore was named to have the highest quality of living in Asia, ranking 25th worldwide.</p>
						<p>-Low unemployment rate.</p>
						<p>-English is also spoken and understood by 75% of the population, and is the official language used in education.</p>
						<p>-Almost one third of the nation’s workforce is made up of foreigners, making it second only to Dubai in proportion of expat workers.</p>
						
						<br>
												<h5><b>Cons</b></h5>
						<p>-Cost of living is quite expensive.</p>
					
					</div>
					
					<div class="image-caption-box">
						<div class="text-box">
							<h2>System of Education</h2>
							<p>The type of host institution you’ll study at depends on both your area of study and the qualification you’d like to be awarded: </p>
							<ul class="list-group">
							<li class="list-group-item">Institute of Technical Education (offers programs that are technical in nature, and is the national authority for setting skills standards for the certification of skills in Singapore)</li>
							<li class="list-group-item">Polytechnic (provide training for specific skills directly geared towards the workplace).</li>
							<li class="list-group-item">Universities (Singapore has a number of universities that are publicly-funded, private, national, and branch campuses of foreign tertiary institutions).</li>
							</ul>
							<p>- A typical Bachelor’s Degree in Singapore is about three-four years in duration for full time students.</p>
							<p>- Students undertaking programs of study in general fields will have to select an area they will specialize in within that field. This area is called a ‘major.</p>
							<p>- A typical Master’s degree for a full-time student is one-two years in length, whilst a PhD may take between two-five years to complete.</p>
							
						</div>
						
						
					</div>
					<div class="image-caption-box">
						<div class="text-box">
							<h2>How to Apply to a Singaporean  University</h2>
							<p>- There are three major intake periods in Singapore: February/March, July/August, and October/November.</p>
							<p>- The main intake period for government-funded universities is around August, with some having a mid-year intake in February</p>
							<p>- Polytechnics typically have their main intakes in April and September, whilst private institutions have multiple intake dates depending on the program.</p>
							<p>- Some universities have different application deadlines depending on specific criteria. </p>
							<p>- Students will be required to prove their English language proficiency, via an IELTS, TOEFL or PTE test score</p>
							<p>- Students are also often required to submit work experience letters and proof of identification documents such as a photocopy of a passport photo page.</p>
							<p>- It is also common for students to have to submit letters of recommendation from previous professors or superiors.</p>
							<p>- At some universities, candidates are not permitted to be students at another institute at the time of application, whilst some universities have different requirements depending on your country of origin.</p>
							<p>- Those applying at a postgraduate level will also need to have completed a Bachelor’s Degree in a relevant field, and will often need to submit a research proposal if applying for a research-based program.</p>
							<p>- Once receiving your university offer, you should apply for a Singapore student’s pass.</p>
							<p>- International students can apply directly to a university online through their website.</p>
							
							
							
						</div>
						
					</div>
					
					<div class="image-caption-box">
						<div class="text-box">
							<h2>Visa</h2>
							<p>- To study in Singapore you will need something that is called a Student’s Pass. Some students will need a visa to enter the country as well as a Student’s Pass.</p>
							<p>- Students from some nationalities will need a visa to enter Singapore. These students may apply for a visa using the Student’s Pass Online Application & Registration (SOLAR+) online system after their host institution has registered them with it.</p>
							<p>- If your visa application is successful, you will be issued a new SOLAR+ application number that you can use to apply for a Student Pass from your home country.</p>
							<p>- You’ll also need to pay an additional visa fee when you collect your Student’s Pass from the Immigration & Checkpoints Authority (ICA) in Singapore.</p>
							<p>- Before you log onto SOLAR+ to complete the eForm 16, you will need to have the following documents and information handy: A registration acknowledgement notices from your host, acknowledging and confirming that they have successfully registered you with the SOLAR+ system, Passport details: passport number, nationality, date of expiry, Your address in Singapore if applicable. If you haven’t sorted out accommodation yet then you can put your university’s registered address, your e-mail address, One recent passport-sized color photograph that has been taken within the last 3 months.</p>
							<p>- Once you have completed the form online through SOLAR+, you need to print a copy of it that you will need to submit in person to the Student’s Pass Unit (ICA) when you go to pick up your pass.</p>
							<p>- If your application is successful, you will be issued an In-Principle Approval (IPA) letter by your host institution. If you require a visa to enter Singapore, it will be attached to this letter. Your IPA letter will act as a provisional Student’s Pass until you go to collect it from an ICA office, and if applicable you can use it to enter the country.</p>
							<p>- Your Student Pass will allow you to work full-time in the school holidays and 16 hours a week in term time for most students.</p>
							<p>- The ICA takes a SGD 30 (US$ 24) non-refundable fee for every application submitted to them.</p>
							<p>- There is a SGD 60 (US$ 47) issuance fee for A Student’s Pass, and students requiring a visa will need to pay a further SGD 30 visa fee.</p>
							
							
						</div>
						
					</div>
					
					<div class="image-caption-box">
						<div class="text-box">
							<h2>Cost of living and scholarhships</h2>
							<p>- Most students opt for off-campus housing organized by student housing agencies, stay in student hostels or rent accommodation privately.</p>
							<p>- Hostels offer single to four-bedded rooms, and provide meals, laundry services, television and internet, and are generally priced at around SGD 140-SGD 440 per month.</p>
							<p>- The estimated monthly cost of a room in a Housing Development Board flat will cost around SGD 400-SGD 1,000 per month.</p>
							<p>- On average however, tuition fees area likely to range from SGD 13,730-SGD 40,010 (US$ 10,885-US$ 31,719) per year.</p>
							<p>- International students may apply for a tuition grant administered by the<a href="https://tgonline.moe.gov.sg/tgis/normal/index.action" target="new"> Ministry of Education</a> to subsidize their fees. Whilst students do not need to pay back the cost of the grant, they are required to work in Singapore upon completion of their studies to contribute to its repayment.</p>
							<p>-On average the cost of living per year in Singapore is $18,350.</p>
							<p>-Even though the cost of living is high there is a lot of scholarships available for international students</p>
							<p>- You can find a list of available programs on the  <a href="http://www.moe.gov.sg/education/scholarships/#header" target="new"> MOE scholarships webpage, as well as a number of links that will lead you to other scholarships awarded by subject area, study level, host university and by external funding bodies.</p>
							<p>- There are a number of higher education institutions in Singapore that offer scholarships specific to their own study programs, details of which are individually retrievable from your host’s website.</p>
							<p>- There are a range of funding options on offer from external companies and funding bodies in Singapore. Arrangements are typically subject-specific, and are offered across all study levels. Most funding arrangements from a company require students to participate in a ‘Bond Agreement’, which means that the student is obliged to work with the company for a set period of time after they graduate. This is called the ‘Bond Period’.</p>
							<p>- You can search for external funding directly using online search engines <b>Scholars4Dev</b> and <b>Bright Sparks</b>, or use the Singapore Scholarship guide’s comprehensive list of external scholarship providers to help you</p>
							<p>- Cost of living in Singapore is 39.52% higher than in Ghana (aggregate data for all cities, rent is not taken into account). Rent in Singapore is 63.54% higher than in Ghana (average data for all cities).</p>
							
							
							
