						<h2><span>Why South Korea?</span></h2>
						<h5><b>Pros</b></h5>
						<p>-Seoul is currently ranked among the world’s top 10 student cities.</p>
						<p>-The South Korean government is making it easier for international students to go abroad and succeed at Korean universities.</p>
						<p>-They offer incentive like the ability for foreign students to work part-time in South Korea, pay the same amount of tuition as Korean students, and have access to hundreds of scholarships.</p>
						<p>-Investment in education and research has been at the heart of the South Korea's growth into the world’s 13th largest economy and the third largest economy within Asia.</p>
						<p>-Low unemployment and great public services like transportation.</p>
						
						<br>
												<h5><b>Cons</b></h5>
						<p>-Korean work culture.</p>
						<p>-Language.</p>
						<p>-Different social culture.</p>
					</div>
					
					<div class="image-caption-box">
						<div class="text-box">
							<h2>System of Education</h2>
							<p>- Most Bachelor courses last four years, medicine, dentistry and oriental medicine degrees usually last for six years.</p>
							<p>- There are two different types of institutions: Private Universities (tuition can be higher at these universities so it's always worth researching that beforehand), Public and National Universities (These institutions are run by local governments or the national Korean government and are often cheaper than private universities).</p>
							<p>- The South Korean academic year begins in March, although many schools take on new students twice a year – in March and September.</p>
							<p>-Unless you want to attend an exchange program or are getting your masters, you will need to have a decent level of Korean proficiency. Most Korean universities will want you to have a TOPIK (Test of Proficiency in Korean) score of at least 4 or better (which is the equivalent to intermediate proficiency).</p>
							<p>-Between 25-30% of classes in universities are to be taught in English.</p>
							
						</div>
						
						
					</div>
					<div class="image-caption-box">
						<div class="text-box">
							<h2>How to Apply to a South Korean  University</h2>
							<p>- Almost every university will require you to apply directly to the university through an online application, just like South Korean students do, so check out their website for more information.</p>
							<p>- English taught programs in South Korea are expanding rapidly, so it will not be difficult to be accepted if you cannot speak fluent Korean.</p>
							<p>-In general universities will request the following documents: Letter of Recommendation and a Counsellor Reference, a copy of your passport, Certificate of Facts concerning the Entry and Exit, issued by the Korean authorities, University application materials, which often include a personal statement, Proof of proficiency in either Korean or English language, depending on the program you are applying to.</p>
							<p>- Applications are usually open from January to February, with admissions decisions released in April.</p>
							<p>- Deadlines for March applications are typically set between September and November, while for September entry, deadlines are often between May to June.</p>
							<p>- Be sure to check with your specific university for more accurate dates.</p>
							
							
							
						</div>
						
					</div>
					
					<div class="image-caption-box">
						<div class="text-box">
							<h2>Visa</h2>
							<p>-Type of visa: </p>
							
							<ul class="list-group">
							<li class="list-group-item">D-2: Visa for Regular Educational Program<br>
							This visa is for any student enrolling in a Bachelors, Masters, or Ph. D program at a junior college, university, or graduate school.</li>
							<li class="list-group-item">D-4: Visa for General Training <br>
							This visa is for any person attending a university-run language school, a qualified educational institute or any other institute other than an academic research institute.</li>
							
							</ul>
							<p>-In general the documents requested are:<br/> valid passport, Visa application forms, depending on which visa you are applying for, Proof of admission or enrolment, Proof of financial stability, Most recent certificate of school record.</p>
							<p>- For your D-2 visa you will be required to pay a processing fee of about US$30 (for single entry) or US$50 (for multiple entry).</p>
							
						</div>
						
					</div>
					
					<div class="image-caption-box">
						<div class="text-box">
							<h2>Cost of living and Scholarships</h2>
							<p>-Dorms are almost always the closest housing to campus, which makes it easy to get to class. Costs range depending on the amount of meals, the location and the university. They can cost anywhere from £200 to £600 per semester.</p>
							<p><b>-Goshiwon:</b> These rooms are very cheap, but also very small. Like dorms, pricing will vary depending on the city you are going to school in. Housing in Seoul is more expensive than housing in smaller cities, but goshiwons are generally inexpensive. Rent can be as low as £115 per month!</p>
							<p><b>-Hasukjip:</b> A hasukjip is similar to a home stay, because you are living in a Korean person’s house, and can offer insight into the culture. They are almost always run independently of universities, but there tend to be more options close to university campuses. The prices are a little more expensive than a goshiwon, and begin around £220 per month, bit can be more expensive.</p>
							<p>-Renting an apartment or house remain the most expensive option, One-rooms, or studio apartments, can be great for students who want more freedom and privacy. The average cost of rent in Seoul is £780 per month, but prices range depending on the type of accommodation and location.</p>
							<p>-Living in your own apartment may come with extra costs like wifi, water, electricity and food.</p>
							<p>-One of the good things about studying in South Korea is that tuition fees are the same for domestic and international students, as part of the national plan to bring more international students to the country.</p>
							<p>-An undergraduate course at a public university costs from US$2,000 to US$4,500 per semester.</p>
							<p>-At a South Korean private university, fees are estimated between US$3,000 and US$6,000 per semester.</p>
							<p>-Average annual tuition fees at the 16 internationally ranked universities in Seoul are US$6,800.</p>
							<p>-Scholarships for international students are widely available from individual universities, covering between 30% and 100% of tuition fees. In addition, a number of governmental scholarships are available, which may also cover air fares and living expenses. One such governmental program is the <a href="http://www.niied.go.kr/eng/contents.do?contentsNo=78&menuNo=349" target="new">Global Korea Scholarship (GKS).</a></p>
							<p>-Cost of living in South Korea is 28.85% higher than in Ghana (aggregate data for all cities, rent is not taken into account). Rent in South Korea is 48.23% lower than in Ghana (average data for all cities).</p>
							
							
