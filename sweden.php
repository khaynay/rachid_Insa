						<h2><span>Why Sweden?</span></h2>
						<h5><b>Pros</b></h5>
						<p>-Sweden’s higher education system is ranked second in the world.</p>
						<p>-Sweden has carved a name for itself as a world leader in creating world famous computer apps and software (Spotify, Skype, Candy Crush, Minecraft).</p>
						<p>-Offer free tuition to students coming from EU countries.</p>
						<p>-It has the highest level of wealth equality in the world.</p>
						<p>-High quality standard of living.</p>
						
						<br>
						<h5><b>Cons</b></h5>
						<p>-Winter can be rude.</p>
						<p>-High taxes.</p>
						<p>-High cost of living.</p>
						
					</div>
					
					<div class="image-caption-box">
						<div class="text-box">
							<h2>System of Education</h2>
							<p>-Swedish universities offer programs across all three cycles, awarding qualifications at undergraduate, postgraduate and doctoral levels.</p>
							<p>-Self-governed (Högskola (University College) and independent, university colleges will generally have the right to award undergraduate qualifications (first and second cycles), but must apply to relevant bodies in order to be able to award those on postgraduate at doctoral levels (third cycle).</p>
							<p>-Programs each have a specific credit requirement that students must meet via completion of either a collection of separate subjects within the same university, or of a full study program with a set quota of compulsory and optional subjects.</p>
							<p>-The Swedish academic year starts at the end of August and finishes at the end of June.</p>
							
						</div>
						
						
					</div>
					<div class="image-caption-box">
						<div class="text-box">
							<h2>How to Apply to a Swedish University</h2>
							<p>-Sweden uses a centralized, pooled admissions system universityadmissions.se that allows all international students to apply electronically for different courses and programs.</p>
							<p>-Acceptance into a program within a given study cycle requires proof of academic qualifications that are equivalent to the credit requirements of the cycle that precedes it.</p>
							<p>-International students must be able to demonstrate proficiency in English, mathematics or Swedish, depending on the nature of their studies.</p>
							
							
							
						</div>
						
					</div>
					
					<div class="image-caption-box">
						<div class="text-box">
							<h2>Visa</h2>
							<p>-All non-EU, Swiss or Nordic students enrolled in study programs longer than three months are required to have a residence permit BEFORE they enter Sweden.</p>
							<p>-You may apply for a residence permit <a href="https://www.migrationsverket.se/English/Private-individuals/Studying-in-Sweden/Universities-and-university-college/How-to-apply/Apply-now.html" target="new">online.</a></p>
							<p>-If you are successful then you will need to make an appointment with your local Swedish embassy or consulate to go and collect your permit in person.</p>
							<p>-In order to obtain a study permit you will need to prove you have been admitted onto a course, that you have comprehensive health insurance and that you can support yourself for the duration of your course. At present, you are required to have SEK8,010 (around US$940) a month for at least 10 months for every year of your stay, in addition to your fees.</p>
								
						</div>
						
					</div>
					
					<div class="image-caption-box">
						<div class="text-box">
							<h2>Cost of living and scholarhships</h2>
							<p>Like everywhere, living costs in Sweden depend on your personal lifestyle and where you live. Here’s an average monthly budget:</p>
							<ul class="list-group">
							<li class="list-group-item">Food: SEK 2,000</li>
							<li class="list-group-item">Accommodation: SEK 3,700</li>
							<li class="list-group-item">Local travel: SEK 550</li>
							<li class="list-group-item">Phone/internet: SEK 300</li>
							<li class="list-group-item">Hobby/leisure, miscellaneous: SEK 1,450</li>
							<li class="list-group-item"><b>Total: SEK 8,000 (around €850 or $1,000).</b></li>
							
							<p>-Non-EU nationals are required to pay admission fees as set by the institution, as well as an application fee of SEK 900. The fee is payable every semester.</p>
							<p>-Generally tuition fees will range from SEK 80,000 to SEK 140,000 per year.</p>
							<p>-The <a href="https://eng.si.se/" target="new">Swedish Institute (SI)</a> is the Swedish government body that awards scholarships to international students.</p>
							<p>-The SI offers a range of scholarships across a number of study areas, mainly at Postgraduate level.</p>
							<p>-Students will also have the chance to participate in a scholarship competition, in which they must complete an academic challenge that demonstrates their skills in a particular study area.</p>
							<p>-Each university will have a list of the scholarships they offer on their website, as well as particulars of the application process.</p>
							<p>-You can also search for externally granted funding through scholarship search engines such as <a href="http://www.scholarships-links.com/" target="new">Scholarship-Links</a> or <a href="http://www.findaphd.com/search/phd.aspx?SAID=643" target="new">Find a PhD.</a></p>
							
