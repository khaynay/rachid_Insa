						<h2><span>Why New Zealand?</span></h2>
						<h5><b>Pros</b></h5>
						<p>-Named one of three nations worldwide where it’s easiest to make friends with locals and integrate into your new community, New Zealanders are known for being exceedingly friendly and welcoming.</p>
						<p>-Since September 2012, the amount of people moving to New Zealand has been steadily on the rise.</p>
						<p>-Named best in the world for environmental progress.</p>
						<p>-On a tertiary student visa, you are allowed a maximum of 20 hours work per week. On vacation, that rises to 40 hours.</p>
						<p>-Universities have a good level.</p>
						<p>-New Zealand qualifications are recognized by all leading higher level institutions worldwide. </p>
						
						<br>
												<h5><b>Cons</b></h5>
						<p>-New Zealand is relatively far from the world and travel expenses can be expensive.</p>
						<p>-The exchange rate can make study pretty expensive due to the high New Zealand dollar. This depends on where you are from.</p>
						<p>-New Zealand schools, universities, and polytechnics charge higher rates for international students.</p>
						<p>-International students also cannot take out student loans unless they obtain residency status which would take a few years.</p>
						<p>-New Zealand may be cheaper than other Western countries but our universities lag behind the US, Britain, Australia, and Singapore in several university league tables.</p>
						<p>-Tight immigration laws and strict tertiary application requirements</p>
						
					</div>
					
					<div class="image-caption-box">
						<div class="text-box">
							<h2>System of Education</h2>
							<p>-The academic year in New Zealand runs from around the end of February to mid-November, varying slightly across institutions.</p>
							<p>-Type of institution: </p>
							<ul class="list-group">
							<li class="list-group-item">Institutes of Technology and Polytechnics (ITPs) (offer qualifications that range from foundation certificate through to postgraduate level that focus on vocational areas of practical and applied training and education.)</li>
							<li class="list-group-item">Private Institutions (run a range of specialized courses in particular disciplines, such as business, travel and tourism, and design.)</li>
							<li class="list-group-item">Wānanga Institutes (Unique to New Zealand, Wānanga institutes offer programs taught in the spirit of Māori culture. Education has a practical focus, with both a ‘Māori kaupapa’ (foundation) and a strong ‘tikanga’ (culture, protocol). </li>
							<li class="list-group-item">Universities (There are eight government funded universities in New Zealand, each offering students the change to study at undergraduate, masters and PhD levels across a range of general study areas.)</li>
							
							</ul>
							<p>-A Bachelor’s degree in New Zealand typically takes three years to complete for full-time students, but can be extended for as long as it takes for a student to meet specific credit requirements.</p>
							<p>-Students also have the option to study two undergraduate degrees at the same time, Double degrees are typically completed within four or five years for a full time student.</p>
							<p>-Honours degrees in New Zealand are offered at post undergraduate and postgraduate levels, At an undergraduate level, an Honours degree is an additional qualification that requires students to complete extra work on top of their Bachelor course load, but without extending the length of their program.</p>
							<p>-Depending on your area of study, classes may be lectures, lab classes, workshops, seminars or tutorial groups.</p>
							
						</div>
						
						
					</div>
					<div class="image-caption-box">
						<div class="text-box">
							<h2>How to Apply to a New Zealand  University</h2>
							<p>-New Zealand universities will require a copy of your passport, relevant academic transcripts and a completed application form specific to your host institution. You may also be required to provide an up-to-date copy of your CV, particularly if applying at a postgraduate level.</p>
							<p>-Depending on your particular program, you may also be required to submit results of English language tests such as the IELTS score card.</p>
							<p>-A minimum IELTS score of 6.0 is required for admission into an undergraduate program, whilst you’ll need at least a 6.5 for admission at a graduate level.</p>
							<p>-International students can apply directly to a university online through their website.</p>
							
							
						</div>
						
					</div>
					
					<div class="image-caption-box">
						<div class="text-box">
							<h2>Visa</h2>
							<p>-If your study program is longer than three months then you will need to apply for a student visa.</p>
							<p>-Whilst you will NOT need to provide proof you have insurance as part of your visa application, you WILL need to sign a declaration as part of the application form that affirms that you will have acceptable insurance for the entire duration of your studies.</p>
							<p>-You can download this form from the Immigration New Zealand <a href="http://www.immigration.govt.nz/migrant/stream/study/application/educationproviders/" target="new">website</a> or visit a New Zealand embassy or consulate and ask for one in person.</p>
							<p>-To apply for a student visa, you will need to include the following documents in your application:</p>
							<ul class="list-group">
							<li class="list-group-item">•	A completed Student Visa Application Form (INZ 1012). </li>
							<li class="list-group-item">•	Visa application fee. </li>
							<li class="list-group-item">•	A valid passport. </li>
							<li class="list-group-item">•	Two passport-sized, color photographs. </li>
							<li class="list-group-item">•	Medical or chest x-ray certificates, ONLY if your study program is longer than six months. </li>
							<li class="list-group-item">•	Police certificate acting as a character reference (if applicable), </li>
							<li class="list-group-item">•	Letter of acceptance from your host institution </li>
							<li class="list-group-item">•	Receipt of the payment of your tuition fees </li>
							<li class="list-group-item">•	A police certificate from your home country and any other country you have lived in for five or more years since the age of 17, ONLY if your study program is longer than 24 months  </li>
							<li class="list-group-item">•	Proof that you will have access to enough funds to support yourself for the duration of your study program: if your study program is less than 36 weeks, you will need to NZ$1,250 (US$1,083) for each month of study, and if it is longer than you will need to provide NZ$15,000 (US$12,996) for each year of study. If you are providing bank statements in your name, the account needs to be at least six months old. You may also need to provide evidence of the source of funds for any deposits into your account over NZ$1000 (US$866). </li>
							
							</ul>
							<p>-Fees vary depending on your home country and the nature of your study program.</p>
							
							
						</div>
						
					</div>
					
					<div class="image-caption-box">
						<div class="text-box">
							<h2>Cost of living and scholarhships</h2>
							<p>-On campus housing is in high demand due to the appealing lifestyle it offers, and so tends to be the most expensive accommodation option for students.</p>
							<p>-on average a student living in halls will be looking to pay around NZ$200-NZ$300 per week.</p>
							<p>-Renting an apartment or room in a share house will probably be your cheapest option,</p>
							<p>-you will need to pay a deposit equivalent to 2-4 weeks rent that you’ll get back once you move out.</p>
							<p>-The rent is likely to be between NZ$120-NZ$230 per week, depending on the size and location of the property.</p>
							<p>-The organization Universities New Zealand provides <a href="http://www.universitiesnz.ac.nz/studying-in-nz/fees" target="new">PDF files</a> with indicative fee estimations for each of the eight main universities each year.</p>
							<p>-The recommended amount to budget for living costs is NZ$20,000-25,000 (~US$14,140-17,680) per year, although you may need to budget more depending on your lifestyle and spending habits.</p>
							<p>-The New Zealand Ministry of Education website has a scholarship <a href="http://www.studyinnewzealand.com/get-started/find-scholarships" target="new">search engine</a> you can use to search for funding that provides details of selection criteria, scholarship value and application advice.</p>
							<p>-Most scholarship options in New Zealand are offered through a specific university, and are always retrievable in full via their website.</p>
							<p>-You can also use this <a href="https://www.studyinnewzealand.govt.nz/" target="new">Scholarship database</a> in order to find the funding that suits your needs</p>
							
